let one = 1
let two = 2

// == igual a -> comparações

console.log(one == two) //false
console.log(one == 1) //true
console.log(one == "1") //true -> olha para o conteúdo e não para o tipo

// != diferente de
console.log(one != two) //true
console.log(one != 1) //false
console.log(one != "1") //false