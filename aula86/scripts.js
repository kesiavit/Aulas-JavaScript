/*
CLASSES

- São uma forma de criar objetos e definir seu comportamento por meio de costrutores e métodos.

- Fornecem uma sintaxe mais agradável para a criação de objetos e herança de protótipos (syntax sugar)


CONSTRUTORES E MÉTODOS

- Classe: modelo para criar objetos;
- Construtor: método especial chamado quando um objeto é instanciado a partir da classe;
- Métodos: funções associadas a objetos e descrevem o comportamento desses objetos.


HERANÇA
- Uma classe pode herdar propriedades e métodos de outra classe, permitindo a reutilização de código.
*/
