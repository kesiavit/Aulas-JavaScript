// Adicionando e removendo um item do array

let users = []

// O "push" adiciona um item ao final do Array.
users.push("Keke")
users.push("Kel")
users.push("Késia")

//O "unshift", faz o texto ir como primeiro na lista
users.unshift("Vitória") 

// Remove do início do Array.
users.shift()

// Remove do final do Array
users.pop()

console.log(users)